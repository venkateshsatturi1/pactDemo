package com.demo.demo.controller;

public class TotalSales {
	
	private int onlineSales;
	private int inStoreSales;
	private int overAllSales;
	public int getOnlineSales() {
		return onlineSales;
	}
	public void setOnlineSales(int onlineSales) {
		this.onlineSales = onlineSales;
	}
	public int getInStoreSales() {
		return inStoreSales;
	}
	public void setInStoreSales(int inStoreSales) {
		this.inStoreSales = inStoreSales;
	}
	public int getOverAllSales() {
		return overAllSales;
	}
	public void setOverAllSales(int overAllSales) {
		this.overAllSales = overAllSales;
	}

}
